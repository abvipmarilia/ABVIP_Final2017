                <p class="text-justify">
                    O Senhor Jesus Cristo deixou bem claro nos evangelhos qual é a nossa responsabilidade social para com o próximo, tanto na parábola do bom samaritano ( Lucas 10:25-37), como no sermão do monte (Mateus 5), onde ambos os relatos Ele deixou bem claro o que devemos fazer, aquém fazer, como e quando fazer, e as consequência da nossa missão diante de uma dessas situações. Em Tiago encontramos a orientação quanto a importância da fé acompanhada das boas obras; ele chega a afirmar que a fé sem obras é morta. Mediante todos esses relatos, não temos como negar a responsabilidade social em ação de todos os cristãos frente ás necessidades espirituais, emocionais e físicas do nosso próximo. A pergunta que merece uma clara e excelente resposta é: “ Como devemos agir para que nossa ação social seja praticada de maneira responsável e sem que venha ferir a dignidade humana, sem que torne um assistencialismo fútil que alimente a miserabilidade humana ou que venha ferir as leis vigentes do país acerca do assunto?</p><p class="text-justify">
Primeiramente consideremos a dignidade humana. Todas as vezes que pensarmos em fazer uma doação para o bem, devemos pensar que a pessoa que vai receber a nossa doação não pode ter sua dignidade ferida. De maneira mais clara e prática, na doação de roupas e alimentos , devemos ter o cuidado de fazermos a seguinte pergunta: “ Esta roupa/alimento que estou doando, eu comeria ou usaria este produto?” Quando fazemos uma doação de produtos resgatados ou vencidos, por exemplo, você não está demostrando respeito ou amor pelo seu próximo. Por isso é extremamente importante, selecionar bem aquilo que for doado. Nada contra doação de roupas usadas, mas que elas estejam em boas condições de uso. Quanto ao alimentos, ter cuidado com datas de validade vencidas ou embalagens rompidas.</p><p class="text-justify">
Outro ponto a considerar é o assistencialismo. O assistencialismo se dá quando alimentamos o estado de carência ou miserabilidade em que a pessoa se encontra, suprindo apenas as suas necessidades emergenciais, sem que lhe proporcione condições para uma mudança de vida. Em uma linguagem mais clara, ao invés de dar o peixe, faz-se necessário ensinar a pessoa a pescar e fornecer-lhe o material necessário para que faça uma boa pescaria. Agindo assim, a pessoa se sentirá capaz e aprenderá a resolver outras necessidades futuras.</p><p class="text-justify">
Hoje, pela graça e misericórdia de Deus, estamos atendendo mais de 150 famílias que, a partir das células, são cadastradas mais de 150 famílias que, a partir das células, são cadastradas e recebem uma cesta básica para suprir suas necessidades. Pela graça e misericórdia do nosso Deus, contamos com algumas parcerias com Instituições especializadas que nos permite encaminhar pessoas em situação em estado de rua, dependência química ou até mesmo realizarmos a reintegração sócio-familiar dos que necessitam deste tipo de apoio. Além das casas de recuperação, mantemos parceria com um lar de crianças e também temos outras ações como encaminhamento de currículos, apoio a famílias dos dependentes internados, auxilio a outras entidades beneficentes com cestas básicas e orientação técnica e administrativa na área da sustentabilidade.</p><p class="text-justify">
Tudo isso só é possível, porque temos a direção e a benção de nosso Deus, a direção da nossa liderança e a participação dos membros da igreja através das células que participam do quilo do amor e entendem o trabalho que está sendo desenvolvido pela coordenação da área social da igreja. É importante ressaltar que todo trabalho social desenvolvido hoje por nossa igreja, está em conformidade com a LOAS ( Lei Orgânica da Assistência Social) que direciona todas as atividades desenvolvidas pelas instituições filantrópicas. E isto é só o começo, o melhor da nossa história está para acontecer.
